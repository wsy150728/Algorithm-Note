<?php
//水仙花数是指一个三位数的各位的立方和加起来等于它本身，求出所有的水仙花数
for ($x = 1; $x <= 9; $x++) {
    for ($y = 0; $y <= 9; $y++) {
        for ($z = 0; $z <= 9; $z++) {
            if ($x * $x * $x + $y * $y * $y + $z * $z * $z == $x * 100 + $y * 10 + $z) {
                echo $x * 100 + $y * 10 + $z . '<br/>';
            }
        }
    }
}